import math

# 원의 둘레 계산 
radius = 3
circ = 2 * math.pi * radius
print("원의 둘레=", circ)

# 팩토리얼 계산
print("7!=", math.factorial(7))

# 2개의 실수가 거의 동일한지를 검사
print("6.999999999와 7의 비교=", math.isclose(6.999999999, 7))

# 자연 로그값 계산
print("log(3.4)=", math.log(3.4))

# 제곱근 계산
print("4의 제곱근=", math.sqrt(4.0))
